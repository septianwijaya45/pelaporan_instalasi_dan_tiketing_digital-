export const CheckNumber = number => {
  if (number == 1) {
    return '01';
  } else if (number == 2) {
    return '02';
  } else if (number == 3) {
    return '03';
  } else if (number == 4) {
    return '04';
  } else if (number == 5) {
    return '05';
  } else if (number == 6) {
    return '06';
  } else if (number == 7) {
    return '07';
  } else if (number == 8) {
    return '08';
  } else if (number == 9) {
    return '09';
  } else {
    return number;
  }
};
